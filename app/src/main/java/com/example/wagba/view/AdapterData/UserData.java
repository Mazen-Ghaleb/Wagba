package com.example.wagba.view.AdapterData;

public class UserData {
    public String firstName, lastName, email, birthdate, gender;

    public UserData(String firstName, String lastName, String email, String birthdate, String gender) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.birthdate = birthdate;
        this.gender = gender;
    }
}
